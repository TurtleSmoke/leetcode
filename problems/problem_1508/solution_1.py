class Solution:
    def rangeSum(self, nums: List[int], n: int, left: int, right: int) -> int:
        heap = []
        for i in range(n):
            total = 0
            for j in range(i, n):
                total += nums[j]
                heapq.heappush(heap, total)

        result = 0
        for _ in range(left - 1):
            heapq.heappop(heap)
        for _ in range(left, right + 1):
            result += heapq.heappop(heap) % (10**9 + 7)
        return result % (10**9 + 7)


