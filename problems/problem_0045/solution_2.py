    def jump(self, nums: List[int]) -> int:
        if len(nums) < 2:
            return 0

        left, right, res = 0, nums[0], 1
        while right < len(nums) - 1:
            res += 1
            left, right = right, max(i + nums[i] for i in range(left, right + 1))

        return res


