class Solution:
    def jump(self, nums: List[int]) -> int:
        dp = [float("inf") for _ in range(len(nums))]
        dp[0] = 0
        for i, n in enumerate(nums):
            for j in range(min(n, len(nums) - i - 1)):
                dp[i + j + 1] = min(dp[i] + 1, dp[i + j + 1])

        return int(dp[-1])


